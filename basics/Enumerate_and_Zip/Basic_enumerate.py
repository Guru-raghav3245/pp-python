# Dictionary
Top_Speed = {"Jesko": 330, "Aventador": 218, "Supra": 155, "Tesla S": 200, "Rimac Nivera": 256, "Valkrie": 249,
             "Speedtail": 250, "Venom": 300}

for index, name in enumerate(Top_Speed):
    print(index, name, Top_Speed[name])


# List
Top_Speed = ["Jesko", "Aventador", "Supra", "Tesla S", "Rimac Nivera", "Valkrie", "Speedtail", "Venom"]
for index, name in enumerate(Top_Speed):
    print(index, name)

# String
Top_Speed = "Supra Mk4"
for index, name in enumerate(Top_Speed):
    print(index, name)
