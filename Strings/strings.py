print(py_charm[-5])
print(py_charm[3])
print(py_charm[-6])

# Going to print 'only is'
print(py_charm[5], py_charm[6])
print(py_charm[5:7])

# Going to print 'is cool'
print(py_charm[5:12])
print(py_charm[5:])

# Going to print the whole string
print(py_charm[0:])

